/*
 ** Checks if the character is space.
 ** Returns 1 if it's a space, 0 if it's not.
*/

int		ft_isspace(int c)
{
	if (c == ' ' || c == '\n' || c == '\t'
		|| c == '\v' || c == '\f' || c == '\r')
		return (1);
	return (0);
}
